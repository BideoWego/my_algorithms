def return_index_of_thing_in_this(array, thing)
  index = nil
  min_index = 0
  max_index = array.length
  while (max_index - min_index) > 1
    middle = ((max_index - min_index) / 2).floor + min_index
    index = middle
    if thing > array[middle]
      min_index = middle + 1
    elsif thing < array[middle]
      max_index = middle - 1
    else
      min_index = max_index = middle
    end
  end
  index
end

